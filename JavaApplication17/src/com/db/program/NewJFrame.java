            String sql = "INSERT INTO OOVPFINAL VALUES ('"+id.getText()+"','"+name.getText()+"','"
                    +gender.getSelectedItem()+"','"+address.getText()+"','"+email.getText()+"','"+phone.getText()+"')";
                                    java.sql.Connection conn = (Connection)config.configDB();
