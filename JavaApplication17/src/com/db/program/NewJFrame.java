    private void tableformMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableformMouseClicked
            // TODO add your handling code here:
            int row = tableform.rowAtPoint(evt.getPoint());
            String KOS_ID = tableform.getValueAt(row, 1).toString();
            id.setText(KOS_ID);
            String NAME = tableform.getValueAt(row, 2).toString();
            name.setText(NAME);
            String GENDER = tableform.getValueAt(row, 3).toString();
            gender.setSelectedItem(GENDER);
            String ADDRESS = tableform.getValueAt(row, 4).toString();
            address.setText(ADDRESS);
            String EMAIL = tableform.getValueAt(row, 5).toString();
            email.setText(EMAIL);
            String PHONE = tableform.getValueAt(row, 6).toString();
            phone.setText(PHONE);
            
                    
    }//GEN-LAST:event_tableformMouseClicked

